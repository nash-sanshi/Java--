package src.main.java.day19;

public class Nineteen9_Person {
    public String name;
    private int age;

    public Nineteen9_Person() {
    }

    public Nineteen9_Person(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
